package service;

public class BookUpdate {

	private int bookNo;
	private int bookPrice;
	private int bookPoint;
	private int bookAmount;
	private String bookCodeName;
	private String publisherName;
	private String bookName;
	private String bookAuthor;
	private String bookOut;
	
	public int getBookNo() {
		return bookNo;
	}
	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}
	
	public int getBookPrice() {
		return bookPrice;
	}
	public void setBookPrice(int bookPrice) {
		this.bookPrice = bookPrice;
	}
	
	public int getBookPoint() {
		return bookPoint;
	}
	public void setBookPoint(int bookPoint) {
		this.bookPoint = bookPoint;
	}
	
	public int getBookAmount() {
		return bookAmount;
	}
	public void setBookAmount(int bookAmount) {
		this.bookAmount = bookAmount;
	}
	
	public String getBookCodeName() {
		return bookCodeName;
	}
	public void setBookCodeName(String bookCodeName) {
		this.bookCodeName = bookCodeName;
	}
	
	public String getPublisherName() {
		return publisherName;
	}
	public void setPublisherName(String publisherName) {
		this.publisherName = publisherName;
	}
	
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	
	public String getBookAuthor() {
		return bookAuthor;
	}
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	
	public String getBookOut() {
		return bookOut;
	}
	public void setBookOut(String bookOut) {
		this.bookOut = bookOut;
	}
	
}
